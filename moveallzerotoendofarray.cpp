class Solution{
public:
	void pushZerosToEnd(int arr[], int n) {
	    // code here
	    int l=0;
	    int r=0;
	    while(r<n){
	     if (arr[r] != 0) {
                int temp = arr[l];
                arr[l] = arr[r];
                arr[r] = temp;
                l++;
            }
            r++;
	    }
	
	}
};

